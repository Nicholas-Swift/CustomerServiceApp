import UIKit

extension UIColor {
    
    static func csaBlack() -> UIColor {
        return UIColor.black
    }
    
    static func csaWhite() -> UIColor {
        return UIColor.white
    }
    
    static func csaBlue() -> UIColor {
        return UIColor(red: 13/255, green: 96/255, blue: 250/255, alpha: 1)
    }
    
    static func csaAlmostWhite() -> UIColor {
        return UIColor(red: 245/255, green: 245/255, blue: 245/255, alpha: 1)
    }
    
    static func csaDarkText() -> UIColor {
        return UIColor(red: 35/255, green: 35/255, blue: 35/255, alpha: 1)
    }
    
    static func csaLightText() -> UIColor {
        return UIColor(red: 165/255, green: 165/255, blue: 165/255, alpha: 1)
    }
    
}
